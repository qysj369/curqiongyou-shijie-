/**
 * 为每个地点 id 生成近似中心点与示意折线（MapLibre 用）。
 * 中心点优先来自 world-countries（中文常用名匹配 + 少量别名），否则用大洲回退。
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import countries from 'world-countries'
import { destinations } from '../src/data/mockData.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const placeOutFile = path.join(__dirname, '../src/data/placeGeo.generated.js')

/** 本站「国家/地区」字段与 world-countries 译名不一致时的 cca3 兜底 */
const CN_COUNTRY_TO_CCA3 = {
  阿联酋: 'ARE',
  北马其顿: 'MKD',
  留尼汪: 'REU',
  波黑: 'BIH',
  多米尼克: 'DMA',
  '刚果（布）': 'COG',
  '刚果（金）': 'COD',
  密克罗尼西亚联邦: 'FSM',
  中非: 'CAF',
}

const CONTINENT_FALLBACK = {
  亚洲: [85, 35],
  欧洲: [15, 50],
  非洲: [20, 5],
  北美: [-95, 40],
  北美洲: [-95, 40],
  南美: [-60, -15],
  南美洲: [-60, -15],
  大洋洲: [135, -25],
  南极洲: [0, -80],
}

const byCca3 = Object.fromEntries(countries.map((c) => [c.cca3, c]))

function latLngToLngLat(latlng) {
  const [lat, lng] = latlng
  return { lng, lat }
}

/** 围绕中心生成 3 点示意折线（非真实轨迹，仅视觉提示「可连接动线」） */
function zigzagLine(lng, lat) {
  const d = 0.45
  return [
    [lng - d * 0.55, lat - d * 0.25],
    [lng + d * 0.15, lat + d * 0.2],
    [lng + d * 0.5, lat - d * 0.15],
  ]
}

function coordsForCountryName(cn) {
  if (CN_COUNTRY_TO_CCA3[cn]) {
    const c = byCca3[CN_COUNTRY_TO_CCA3[cn]]
    if (c?.latlng) return latLngToLngLat(c.latlng)
  }
  for (const c of countries) {
    const z = c.translations?.zho?.common
    if (z === cn && c.latlng) return latLngToLngLat(c.latlng)
  }
  return null
}

const out = {}
for (const d of destinations) {
  let { lng, lat } = coordsForCountryName(d.country) || {}
  if (lng == null || lat == null) {
    const fb = CONTINENT_FALLBACK[d.continent] || [0, 20]
    ;[lng, lat] = fb
  }
  const line = zigzagLine(lng, lat)
  out[d.id] = { lng, lat, line }
}

const placeBody = `/* eslint-disable */
// Generated by scripts/generate-place-centroids.mjs. Do not edit.
/** @type {Record<string, { lng: number, lat: number, line: [number, number][] }>} */
export const PLACE_GEO_BY_ID = ${JSON.stringify(out, null, 2)}
`

fs.writeFileSync(placeOutFile, placeBody, 'utf8')
console.log('[generate-place-centroids] wrote', placeOutFile, 'keys:', Object.keys(out).length)
