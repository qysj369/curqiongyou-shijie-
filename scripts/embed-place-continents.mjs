/**
 * 从 mockData 生成 CONTINENT_BY_PLACE_NAME，供 coverGeoPolicy 使用。
 * 运行: node scripts/embed-place-continents.mjs
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')

const { destinations } = await import('../src/data/mockData.js')

const o = {}
for (const d of destinations) {
  if (d?.name && d?.continent) o[d.name] = d.continent
}

const placeOut = `/** Generated by scripts/embed-place-continents.mjs. Do not edit. */
export const CONTINENT_BY_PLACE_NAME = ${JSON.stringify(o, null, 2)}
`

const placeOutPath = path.join(root, 'src/data/placeContinents.generated.js')
fs.writeFileSync(placeOutPath, placeOut, 'utf8')
console.log('Wrote', placeOutPath, 'keys:', Object.keys(o).length)
