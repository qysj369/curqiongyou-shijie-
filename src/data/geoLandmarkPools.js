function uniq(arr) {
  return [...new Set(arr.filter(Boolean))]
}

function buildPoolForDestination(name) {
  if (name === '中国') {
    return uniq([...CHINA_LANDMARK_POOL, CAPITAL_ZH_BY_DESTINATION[name]])
  }
  const cap = CAPITAL_ZH_BY_DESTINATION[name]
  const extra = EXTRA_LANDMARKS[name] || []
  if (!cap && extra.length) return uniq(extra)
  if (!cap) return uniq(extra)
  return uniq([cap, ...extra])
}

/** 与 ALL_DESTINATION_NAMES 一一对齐 */
export const LANDMARKS_BY_DESTINATION = Object.fromEntries(
  ALL_DESTINATION_NAMES.map((name) => [name, buildPoolForDestination(name)]),
)

function strHash(s) {
  let h = 2166136261
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i)
    h = Math.imul(h, 16777619)
  }
  return Math.abs(h)
}

/**
 * 若标题已含目的地名但不含该地任一地标，则在国名后插入「·地标名」便于核对。
 * 用地标池下标 = hash(目的地+序号)，减轻「篇与篇之间地标重复」。
 */
export function ensureLandmarkInTitle(destinationName, seqForDest, title) {
  const pool = LANDMARKS_BY_DESTINATION[destinationName]
  if (!pool?.length) return title
  if (pool.some((kw) => title.includes(kw))) return title
  const kw = pool[strHash(`${destinationName}:${seqForDest}`) % pool.length]
  const idx = title.indexOf(destinationName)
  if (idx === -1) return `${destinationName}·${kw}·${title}`
  const end = idx + destinationName.length
  return `${title.slice(0, end)}·${kw}${title.slice(end)}`
}
