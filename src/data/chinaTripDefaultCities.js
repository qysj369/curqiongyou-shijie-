function uniquePool() {
  return [...new Set(CHINA_TRIP_DEFAULT_CITIES)]
}

/**
 * @returns {{ destination: string, departCity: string }}
 */
export function pickRandomChinaTripDefaults() {
  const pool = uniquePool()
  if (!pool.length) return { destination: '杭州', departCity: '上海' }
  const dest = pool[Math.floor(Math.random() * pool.length)]
  let depart = pool[Math.floor(Math.random() * pool.length)]
  let n = 0
  while (depart === dest && pool.length > 1 && n < 24) {
    depart = pool[Math.floor(Math.random() * pool.length)]
    n += 1
  }
  if (depart === dest && pool.length > 1) {
    depart = pool.find((c) => c !== dest) || depart
  }
  return { destination: dest, departCity: depart }
}
