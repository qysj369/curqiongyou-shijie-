/**
 * 意图变体：列表摘要 + 详情短正文（非通用「路线骨架」）。
 * @param {{ city?: string, days?: number, budget?: number, intentSummary?: string, intentKind?: string, guideAnchorId?: string, title?: string }} variant
 * @param {{ id?: string, title?: string, city?: string } | null} anchor
 */
export function buildIntentVariantArticleContent(variant, anchor) {
  const city = variant.city || anchor?.city || '当地'
  const days = variant.days || 3
  const budget = variant.budget || 2000
  const summary = variant.intentSummary || `${city}${days}日意图速览，人均约 ¥${budget}。`
  const anchorId = variant.guideAnchorId || anchor?.id
  const anchorTitle = anchor?.title || `${city}深度路书`
  const bullets = INTENT_HIGHLIGHTS[variant.intentKind] || INTENT_HIGHLIGHTS.default
  const highlightLines = bullets(city, days)

  const anchorBlock = anchorId
    ? `完整分日正文、预算拆分与避坑见 **[${anchorTitle}](/routes/${anchorId})**（深度路书）。`
    : '可在本站筛选该城市「深度路书」查看分日正文。'

  return `
## 意图摘要
${summary}

## 本版适合谁
${INTENT_AUDIENCE[variant.intentKind]?.(city, days) || `计划 ${days} 天游玩${city}、希望按明确意图安排动线的旅人。`}

## 参考预算与天数
- **天数**：${days} 天
- **人均预算**：约 ¥${budget}（相对同城深度路书按交通/住宿习惯微调）

## 动线要点
${highlightLines.map((line) => `- ${line}`).join('\n')}

## 与深度路书的关系
本篇为意图变体的**速览说明**，不含自动生成的通用穷游骨架。${anchorBlock}

## 搭配 Trip
用 Trip 按「${variant.tags?.[0] || '穷游'}」意图生成分日行程后，再对照深度路书微调每日节奏与取舍。
`.trim()
}

