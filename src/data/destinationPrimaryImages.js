export const PRIMARY_IMAGE_BY_COUNTRY = {
  ...PRIMARY_IMAGE_BASE,
  ...PRIMARY_IMAGE_GEO_FIXES,
}

function photoIdFromUrl(url) {
  const m = /images\.unsplash\.com\/photo-(\d+)-/i.exec(url || '')
  return m ? m[1] : null
}

function urlNotBanned(url) {
  const id = photoIdFromUrl(url)
  if (!id) return true
  return !COVER_BANNED_UNSPLASH_IDS.has(id)
}

/** 列表/详情统一用地理标志修正后的主图；缺省时回退 mock 数据自带 image */
export function getDestinationHeroImageUrl(dest) {
  const name = typeof dest === 'string' ? dest : dest?.name
  if (typeof name === 'string' && PRIMARY_IMAGE_BY_COUNTRY[name]) {
    const u = PRIMARY_IMAGE_BY_COUNTRY[name]
    if (urlNotBanned(u)) return u
  }
  if (typeof dest === 'object' && dest?.image && urlNotBanned(dest.image)) return dest.image
  return undefined
}
