const SEARCH_LOOKUP = Object.assign(
  {},
  DESTINATION_SEARCH_ALIASES,
  ISO_ALPHA2_TO_ZH,
  IATA_THREE_LETTER_TO_ZH,
  MULTIWORD_EN_TO_ZH,
)

const QUERY_SPLIT = /[\s,，、;/]+/

/**
 * 去掉拉丁字母组合音标（München→Munchen, São→Sao），与机票/OTA 拼写习惯对齐。
 */
export function stripLatinDiacritics(val) {
  const s = (val || '').toString()
  return s.normalize('NFD').replace(/\p{M}/gu, '').normalize('NFC')
}

export function normalizeSearchText(val) {
  const base = (val || '').toString().toLowerCase().normalize('NFC').trim()
  return stripLatinDiacritics(base)
}

/** 先匹配最长英文词组，再按空白/标点切词（符合「United States」「Sri Lanka」等全球检索习惯） */
export function tokenizeQuery(rawQuery) {
  const s = normalizeSearchText(rawQuery)
  if (!s) return []
  const phrases = Object.keys(MULTIWORD_EN_TO_ZH).sort((a, b) => b.length - a.length)
  const out = []
  let i = 0
  while (i < s.length) {
    while (i < s.length && /[\s,，、;/]/.test(s[i])) i++
    if (i >= s.length) break
    let hit = false
    for (const ph of phrases) {
      if (
        s.startsWith(ph, i) &&
        (i + ph.length === s.length || /[\s,，、;/]/.test(s[i + ph.length]))
      ) {
        out.push(ph)
        i += ph.length
        hit = true
        break
      }
    }
    if (!hit) {
      const rest = s.slice(i)
      const m = rest.match(/^[^\s,，、;/]+/)
      if (m) {
        out.push(m[0])
        i += m[0].length
      } else i++
    }
  }
  return out
}

function expandTermNeedles(term) {
  const t = normalizeSearchText(term)
  if (!t) return []
  const needles = new Set([t])
  const zh = SEARCH_LOOKUP[t]
  if (zh) needles.add(normalizeSearchText(zh))
  return [...needles].filter(Boolean)
}

function blobMatchesAllTerms(blobNorm, terms) {
  return terms.every((term) => {
    const needles = expandTermNeedles(term)
    return needles.some((n) => n && blobNorm.includes(n))
  })
}

/**
 * 返回用于「整句 OR」兼容的旧版针脚列表：整句 + 分词 + 双连词 + 三连词别名展开。
 * 新逻辑以分词 AND 为主（matchesGuideCard / matchesDestinationCard）。
 */
export function searchQueryNeedles(rawQuery) {
  const q = normalizeSearchText(rawQuery)
  if (!q) return []
  const needles = new Set([q])
  const fullZh = SEARCH_LOOKUP[q]
  if (fullZh) needles.add(normalizeSearchText(fullZh))
  const tokens = q.split(QUERY_SPLIT).filter(Boolean)
  for (const t of tokens) {
    needles.add(t)
    const z = SEARCH_LOOKUP[t]
    if (z) needles.add(normalizeSearchText(z))
  }
  for (let i = 0; i < tokens.length - 1; i++) {
    const bi = `${tokens[i]} ${tokens[i + 1]}`
    needles.add(bi)
    const z = SEARCH_LOOKUP[bi]
    if (z) needles.add(normalizeSearchText(z))
  }
  for (let i = 0; i < tokens.length - 2; i++) {
    const tri = `${tokens[i]} ${tokens[i + 1]} ${tokens[i + 2]}`
    needles.add(tri)
    const z = SEARCH_LOOKUP[tri]
    if (z) needles.add(normalizeSearchText(z))
  }
  return [...needles].filter(Boolean)
}

export function textMatchesNeedles(text, needles) {
  if (!needles.length) return true
  const hay = normalizeSearchText(text)
  return needles.some((n) => n && hay.includes(n))
}

/** 攻略卡片：标题、目的地、作者、标签；多词时各词均需命中（AND），与常见搜索引擎一致 */
export function matchesGuideCard(item, rawQuery) {
  const q = normalizeSearchText(rawQuery)
  if (!q) return true
  const terms = tokenizeQuery(q)
  if (!terms.length) return true
  const tags = item.tags || []
  const blob = [item.title, item.destination, item.author, ...tags].filter(Boolean).join(' ')
  return blobMatchesAllTerms(normalizeSearchText(blob), terms)
}

/** 目的地：名称、国家、地区、标签；多词 AND */
export function matchesDestinationCard(dest, rawQuery) {
  const q = normalizeSearchText(rawQuery)
  if (!q) return true
  const terms = tokenizeQuery(q)
  if (!terms.length) return true
  const blob = [dest.name, dest.country, dest.region, dest.continent, ...(dest.tags || [])]
    .filter(Boolean)
    .join(' ')
  return blobMatchesAllTerms(normalizeSearchText(blob), terms)
}
